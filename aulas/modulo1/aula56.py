# Split e join

# Split: Divide uma string em uma lista de substrings, separados por um separador
frase = 'O Python eh uma linguagem de programacao multiparadigma. Linguagens multiplas podem ser usadas para resolver muitos problemas.'

palavras = frase.split()

print(palavras)

# Join: Junta uma lista de substrings em uma string
frase = ' '.join(palavras)

print(frase)